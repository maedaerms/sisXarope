    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        // TODO add your handling code here:
        DAO dao = new DAOFornecedor();
        
        if(dao instanceof DAO)
        {
            Fornecedor fornecedor = new Fornecedor();

            fornecedor.setNome(jTxtNome.getText());
            fornecedor.setCnpj(jTxtCnpj.getText());
            fornecedor.setIe(jTxtIe.getText());
            fornecedor.setEndereco(jTxtEndereco.getText());
            fornecedor.setTelefone(jTxtTelefone.getText());
            
            dao.salvar(fornecedor);
        }
        
            this.limparCampos();

    }//GEN-LAST:event_jBtnSalvarActionPerformed

