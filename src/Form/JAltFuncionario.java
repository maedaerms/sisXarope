    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        // TODO add your handling code here:
        DAO dao = new DAOFuncionario();
        
        if(dao instanceof DAO)
        {
            Funcionario funcionario = new Funcionario();

            funcionario.setId(Integer.parseInt(jTxtCodigo.getText()));
            funcionario.setNome(jTxtNome.getText());
            funcionario.setRg(jTxtRg.getText());
            funcionario.setCpf(jTxtCpf.getText());
            funcionario.setEndereco(jTxtEndereco.getText());
            funcionario.setSexo(jTxtSexo.getText());
            funcionario.setCargo(jTxtCargo.getText());
            funcionario.setSalario(Double.parseDouble(jTxtSalario.getText()));
            funcionario.setNascimento(jTxtNascimento.getText());
            funcionario.setAdmissao(jTxtAdmissao.getText());
            
            dao.atualizar(funcionario);
        }
        
            this.limparCampos();
    }//GEN-LAST:event_jBtnAlterarActionPerformed

