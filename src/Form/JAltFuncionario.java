    private void jTxtCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtCodigoKeyReleased
        // TODO add your handling code here:
        if ("".equals(jTxtCodigo.getText()))
        {

        }
        else
        {
            Funcionario f = (Funcionario) new DAOFuncionario().getPorID(Integer.parseInt(jTxtCodigo.getText()));
            jTxtNome.setText(f.getNome());
            jTxtRg.setText(f.getRg());
            jTxtCpf.setText(f.getCpf());
            jTxtEndereco.setText(f.getEndereco());
            jTxtSexo.setText(f.getSexo());
            jTxtCargo.setText(f.getCargo());
            jTxtSalario.setText(Double.toString(f.getSalario()));
            jTxtNascimento.setText(f.getNascimento());
            jTxtAdmissao.setText(f.getAdmissao());
        }
    }//GEN-LAST:event_jTxtCodigoKeyReleased

